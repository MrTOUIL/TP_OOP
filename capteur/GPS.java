

package capteur;

public class GPS extends Capteur {
    private double x;
    private double y;

    public GPS() {
        super();
    }

    public GPS(double x, double y) {
        super();
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}